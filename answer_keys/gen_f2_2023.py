    story.append(S(10))
    story.append(Paragraph("mytzstudies.com", styles['CoverURL']))
    story.append(S(30))
    fs = ParagraphStyle('FI', fontName='Helvetica-Bold', fontSize=11, textColor=DARK_BLUE, alignment=TA_CENTER)
