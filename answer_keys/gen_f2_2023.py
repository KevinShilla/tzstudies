    story.append(Paragraph("<b>(b) Evaluate (2/25) x 0.737 correct to:</b>", sub))
    story.append(S(4))
    story.append(Paragraph("<b>Step 1:</b> First, convert 2/25 to a decimal:", st))
    story.append(Paragraph("2/25 = 2 divided by 25 = 0.08", st))
    story.append(S(2))
    story.append(Paragraph("<b>Step 2:</b> Multiply: 0.08 x 0.737 = 0.05896", st))
    story.append(S(4))
    story.append(Paragraph("<b>(i) To one significant figure:</b>", sub))
    story.append(Paragraph("0.05896 - the first significant figure is 5 (we skip the leading zeros and the 0). Looking at the next digit (8), since 8 is 5 or more, round up.", st))
    story.append(ans("Answer: 0.06"))
    story.append(S(4))
    story.append(Paragraph("<b>(ii) To three decimal places:</b>", sub))
    story.append(Paragraph("0.05896 - look at 3 digits after decimal: 058. The next digit is 9, so round up the 8.", st))
    story.append(ans("Answer: 0.059"))
    story.append(S(6))
    story.append(warn("Significant figures and decimal places are different! In 0.05896: there are 4 significant figures (5, 8, 9, 6) but the zeros before 5 are NOT significant - they are just placeholders."))
    story.append(S(6))
    story.append(summary("Total marks: 10. Key topics: Multiples, LCM, decimal multiplication, significant figures, decimal places. Study tip: Leading zeros are never significant."))
    story.append(PageBreak())

    # ===== Q2: Fractions and Percentages =====
    story.append(qheader(2, "Fractions and Percentage Increase", styles))
    story.append(S(8))
    story.append(Paragraph("<b>(a) Arrange 2/3, 4/7, 3/8, and 5/9 in ascending order (smallest to largest).</b>", sub))
    story.append(S(4))
    story.append(Paragraph("To compare fractions, we need to give them all the same bottom number (denominator).", body))
    story.append(S(4))
    story.append(Paragraph("<b>Step 1:</b> Find the LCD (Lowest Common Denominator) of 3, 7, 8, and 9.", st))
    story.append(Paragraph("LCD = 504", st))
    story.append(S(2))
    story.append(Paragraph("<b>Step 2:</b> Convert each fraction:", st))
    story.append(Paragraph("2/3 = (2 x 168)/(3 x 168) = 336/504", st))
    story.append(Paragraph("4/7 = (4 x 72)/(7 x 72) = 288/504", st))
    story.append(Paragraph("3/8 = (3 x 63)/(8 x 63) = 189/504", st))
    story.append(Paragraph("5/9 = (5 x 56)/(9 x 56) = 280/504", st))
    story.append(S(2))
    story.append(Paragraph("<b>Step 3:</b> Now compare the top numbers (numerators):", st))
    story.append(Paragraph("189 &lt; 280 &lt; 288 &lt; 336", st))
    story.append(Paragraph("So: 3/8 &lt; 5/9 &lt; 4/7 &lt; 2/3", st))
    story.append(S(4))
    story.append(ans("Final Answer: 3/8, 5/9, 4/7, 2/3 (ascending order)"))
    story.append(S(8))

    story.append(Paragraph("<b>(b) In 2016, Mericho village population was 2,800. In 2017 it increased by 8%. What was the population in 2017?</b>", sub))
    story.append(S(4))
    story.append(Paragraph("<b>Step 1:</b> Calculate the increase:", st))
    story.append(Paragraph("Increase = 8% of 2,800", st))
    story.append(Paragraph("= (8/100) x 2,800", st))
    story.append(Paragraph("= 0.08 x 2,800", st))
    story.append(Paragraph("= 224 people", st))
    story.append(S(2))
    story.append(Paragraph("<b>Step 2:</b> Add increase to original:", st))
    story.append(Paragraph("Population in 2017 = 2,800 + 224 = 3,024", st))
    story.append(S(4))
    story.append(ans("Final Answer: Population in 2017 = 3,024"))
    story.append(S(6))
    story.append(tip("A shortcut for percentage increase: multiply by (100% + increase%). So 2,800 x 1.08 = 3,024. This works because 108% = 100% (original) + 8% (increase)."))
    story.append(S(6))
    story.append(summary("Total marks: 10. Key topics: Comparing fractions, LCD, percentage increase. Study tip: To compare fractions, always convert to the same denominator first."))
    story.append(PageBreak())

    # ===== Q3: Units and Profit =====
    story.append(qheader(3, "Unit Conversion and Profit", styles))
    story.append(S(8))
    story.append(Paragraph("<b>(a) 1,000 tonnes of maize shared among 25 schools. How many kilograms each?</b>", sub))
    story.append(S(4))
    story.append(Paragraph("<b>Step 1:</b> Convert tonnes to kilograms.", st))
    story.append(Paragraph("1 tonne = 1,000 kg", st))
    story.append(Paragraph("1,000 tonnes = 1,000 x 1,000 = 1,000,000 kg", st))
    story.append(S(2))
    story.append(Paragraph("<b>Step 2:</b> Divide equally among 25 schools:", st))
    story.append(Paragraph("Each school = 1,000,000 / 25 = 40,000 kg", st))
    story.append(S(4))
    story.append(ans("Final Answer: Each school gets 40,000 kg"))
    story.append(S(8))

    story.append(Paragraph("<b>(b) Radio bought for Tsh. 80,000 and sold at 20% profit. Find profit and selling price.</b>", sub))
    story.append(S(4))
    story.append(Paragraph("<b>Step 1:</b> Calculate the profit:", st))
    story.append(Paragraph("Profit = 20% of Cost Price", st))
    story.append(Paragraph("= (20/100) x 80,000", st))
    story.append(Paragraph("= 0.20 x 80,000", st))
    story.append(Paragraph("= Tsh. 16,000", st))
    story.append(S(2))
    story.append(Paragraph("<b>Step 2:</b> Calculate selling price:", st))
    story.append(Paragraph("Selling Price = Cost Price + Profit", st))
    story.append(Paragraph("= 80,000 + 16,000 = Tsh. 96,000", st))
    story.append(S(4))
    story.append(ans("Final Answer: Profit = Tsh. 16,000. Selling Price = Tsh. 96,000"))
    story.append(S(6))
    story.append(warn("Profit percentage is ALWAYS calculated based on the COST PRICE (buying price), not the selling price. A common exam mistake is calculating 20% of the selling price instead."))
    story.append(S(6))
    story.append(summary("Total marks: 10. Key topics: Unit conversion (tonnes to kg), profit and selling price. Study tip: Selling Price = Cost Price + Profit. Loss = Cost Price - Selling Price."))
    story.append(PageBreak())

    # ===== Q4: Trapezium Angles and Area =====
    story.append(qheader(4, "Trapezium Angles and Square Area", styles))
    story.append(S(8))
