    story.append(Paragraph("Angle A + angle b = 180 degrees", st))
    story.append(Paragraph("60 + b = 180", st))
    story.append(Paragraph("b = 120 degrees", st))
    story.append(S(2))
    story.append(Paragraph("<b>Step 3:</b> Find angle c (interior angle at C):", st))
    story.append(Paragraph("Angle D and angle C are co-interior angles.", st))
    story.append(Paragraph("40 + c = 180", st))
    story.append(Paragraph("c = 140 degrees", st))
