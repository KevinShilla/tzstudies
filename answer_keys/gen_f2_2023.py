    story.append(ft)
    story.append(S(40))
    story.append(Paragraph("Share this with a friend. Every student deserves free study materials.", styles['CoverShare']))
    story.append(S(10))
    story.append(HRFlowable(width="60%", thickness=0.5, color=BORDER_GRAY, spaceAfter=10))
    story.append(Paragraph("Made with mytzstudies.com", styles['CoverFooter']))
    story.append(PageBreak())

    # ========== TITLE ==========
    story.append(S(100))
    story.append(Paragraph("BASIC MATHEMATICS - FORM TWO", styles['TitlePageHeading']))
    story.append(S(10))
    story.append(Paragraph("National Assessment 2023 - Answer Key", styles['TitlePageSub']))
    story.append(S(20))
    info = [["Subject", "Basic Mathematics"], ["Code", "041"], ["Level", "Form Two"], ["Year", "2023"], ["Exam Board", "NECTA"], ["Type", "Answer Key"], ["Total Questions", "10 (10 marks each)"]]
    it = Table(info, colWidths=[150, 250])
    it.setStyle(TableStyle([('BACKGROUND', (0, 0), (0, -1), LIGHT_BLUE_BG), ('TEXTCOLOR', (0, 0), (0, -1), DARK_BLUE), ('FONTNAME', (0, 0), (0, -1), 'Helvetica-Bold'), ('FONTNAME', (1, 0), (1, -1), 'Helvetica'), ('FONTSIZE', (0, 0), (-1, -1), 12), ('GRID', (0, 0), (-1, -1), 0.5, BORDER_GRAY), ('TOPPADDING', (0, 0), (-1, -1), 6), ('BOTTOMPADDING', (0, 0), (-1, -1), 6), ('LEFTPADDING', (0, 0), (-1, -1), 10)]))
    story.append(it)
    story.append(PageBreak())

    # ===== Q1: Multiples and Decimals =====
    story.append(qheader(1, "Multiples and Decimal Operations", styles))
    story.append(S(8))
    story.append(Paragraph("<b>(a) List the first twelve multiples of 4 and 5 and identify the common multiples.</b>", sub))
    story.append(S(4))
    story.append(Paragraph("A multiple is what you get when you multiply a number by 1, 2, 3, 4, and so on.", body))
    story.append(S(4))
    story.append(Paragraph("<b>Step 1:</b> List the first 12 multiples of 4:", st))
    story.append(Paragraph("4, 8, 12, 16, 20, 24, 28, 32, 36, 40, 44, 48", st))
    story.append(S(2))
    story.append(Paragraph("<b>Step 2:</b> List the first 12 multiples of 5:", st))
    story.append(Paragraph("5, 10, 15, 20, 25, 30, 35, 40, 45, 50, 55, 60", st))
    story.append(S(2))
    story.append(Paragraph("<b>Step 3:</b> Find numbers that appear in BOTH lists:", st))
    story.append(Paragraph("20 appears in both lists (4 x 5 = 20 and 5 x 4 = 20)", st))
