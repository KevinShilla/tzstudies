    # ===== Q5: Linear and Quadratic Equations =====
    story.append(qheader(5, "Linear Equation and Completing the Square", styles))
    story.append(S(8))
    story.append(Paragraph("<b>(a) If (6y + 1)/4 = 5(y + 5)/6, find y correct to three significant figures.</b>", sub))
    story.append(S(4))
    story.append(Paragraph("<b>Step 1:</b> Cross multiply to remove the fractions:", st))
    story.append(Paragraph("6 x (6y + 1) = 4 x 5(y + 5)", st))
    story.append(Paragraph("6(6y + 1) = 20(y + 5)", st))
    story.append(S(2))
    story.append(Paragraph("<b>Step 2:</b> Expand both sides:", st))
    story.append(Paragraph("36y + 6 = 20y + 100", st))
    story.append(S(2))
    story.append(Paragraph("<b>Step 3:</b> Collect like terms:", st))
    story.append(Paragraph("36y - 20y = 100 - 6", st))
    story.append(Paragraph("16y = 94", st))
    story.append(S(2))
    story.append(Paragraph("<b>Step 4:</b> Divide both sides by 16:", st))
    story.append(Paragraph("y = 94/16 = 5.875", st))
    story.append(S(2))
    story.append(Paragraph("<b>Step 5:</b> Round to 3 significant figures:", st))
    story.append(Paragraph("5.875 has 4 significant figures. To 3 sig fig: 5.88 (the 7 rounds up the second 7...)", st))
    story.append(Paragraph("5.875 to 3 sig fig = 5.88", st))
    story.append(S(4))
    story.append(ans("Final Answer: y = 5.88 (to 3 significant figures)"))
    story.append(S(8))

    story.append(Paragraph("<b>(b) Solve 3x<super>2</super> - 7x - 6 = 0 by completing the square.</b>", sub))
    story.append(S(4))
    story.append(Paragraph("<b>Step 1:</b> Divide everything by 3 (the number in front of x<super>2</super>):", st))
    story.append(Paragraph("x<super>2</super> - (7/3)x - 2 = 0", st))
    story.append(S(2))
    story.append(Paragraph("<b>Step 2:</b> Move the constant to the right side:", st))
    story.append(Paragraph("x<super>2</super> - (7/3)x = 2", st))
    story.append(S(2))
    story.append(Paragraph("<b>Step 3:</b> To complete the square, take half of the coefficient of x, then square it:", st))
    story.append(Paragraph("Half of -7/3 = -7/6", st))
    story.append(Paragraph("(-7/6)<super>2</super> = 49/36", st))
    story.append(S(2))
    story.append(Paragraph("<b>Step 4:</b> Add 49/36 to both sides:", st))
    story.append(Paragraph("x<super>2</super> - (7/3)x + 49/36 = 2 + 49/36", st))
    story.append(Paragraph("(x - 7/6)<super>2</super> = 72/36 + 49/36 = 121/36", st))
    story.append(S(2))
    story.append(Paragraph("<b>Step 5:</b> Take square root of both sides:", st))
    story.append(Paragraph("x - 7/6 = +/- 11/6", st))
    story.append(S(2))
    story.append(Paragraph("<b>Step 6:</b> Solve for x:", st))
    story.append(Paragraph("x = 7/6 + 11/6 = 18/6 = 3", st))
    story.append(Paragraph("OR x = 7/6 - 11/6 = -4/6 = -2/3", st))
    story.append(S(4))
    story.append(ans("Final Answer: x = 3 or x = -2/3"))
